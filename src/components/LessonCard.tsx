'use client'

import Link from 'next/link'
import { useSyncExternalStore } from 'react'
import type { Lesson } from '@/lib/lessons'

interface LessonCardProps {
  readonly lesson: Lesson
}

export default function LessonCard({ lesson }: LessonCardProps) {
  const completed = useSyncExternalStore(
    subscribeToLessonProgress,
    () => getLessonCompletion(lesson.slug),
    () => false,
  )

  return (
    <Link
      href={`/lessons/${lesson.slug}`}
      className="group relative flex flex-col overflow-hidden rounded-xl border border-[var(--border)] bg-[var(--bg-card)] transition-all hover:border-[var(--accent)]/50 hover:bg-[var(--bg-card-hover)] hover:shadow-lg hover:shadow-orange-500/5"
    >
      <div className="flex h-40 flex-col justify-between bg-[var(--bg-secondary)] p-5">
        <div className="flex h-12 w-12 items-center justify-center rounded-xl bg-[var(--text-primary)]/10 text-[var(--text-primary)]">
          <LessonIcon slug={lesson.slug} />
        </div>
        <h3 className="text-base font-semibold leading-snug text-[var(--text-primary)] group-hover:text-[var(--accent)] transition-colors">
          {lesson.title}
        </h3>
      </div>
      <div className="flex flex-1 flex-col gap-1 p-4">
        <span className="text-xs font-mono font-semibold text-[var(--accent)]">
          Lesson {String(lesson.order).padStart(2, '0')}
        </span>
        <p className="text-xs text-[var(--text-secondary)] leading-relaxed">
          {lesson.description}
        </p>
      </div>
      {completed && (
        <div className="absolute top-3 right-3 flex h-6 w-6 items-center justify-center rounded-full bg-green-600 text-white text-xs">
          ✓
        </div>
      )}
    </Link>
  )
}

function subscribeToLessonProgress(callback: () => void) {
  window.addEventListener('storage', callback)
  return () => window.removeEventListener('storage', callback)
}

function getLessonCompletion(slug: string) {
  const progress = JSON.parse(localStorage.getItem('lesson-progress') ?? '{}')
  return progress[slug] === true
}

